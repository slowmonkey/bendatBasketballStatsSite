    Opponent SMALLINT NOT NULL,
    Game SMALLINT NOT NULL,
    FTM SMALLINT NOT NULL,
    FTA SMALLINT NOT NULL,
    2PM SMALLINT NOT NULL,
    2PA SMALLINT NOT NULL,
    3PM SMALLINT NOT NULL,
    3PA SMALLINT NOT NULL,
    OReb SMALLINT NOT NULL,
    DReb SMALLINT NOT NULL,
    Assists SMALLINT NOT NULL,
    Steals SMALLINT NOT NULL,
    Blocks SMALLINT NOT NULL,
    TOVs SMALLINT NOT NULL,
    Win/Loss VARCHAR(1) NOT NULL
)